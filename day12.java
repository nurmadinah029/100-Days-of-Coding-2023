package seratushari;
public class day12 {
    public static void main(String[] args) {
        System.out.println("HELLOW WORD ");  
        double angka1,angka2;
        angka1 = 25;
        angka2 = 45;
        //saya menggunakan metode penugasan
        
       angka1 += angka2; // ini sama saja dengan angka1 + angka2 
       System.out.println(angka1);
       angka1 -= angka2; // ini sama saja dengan angka1 - angka2 
       System.out.println(angka1);
       angka1 *= angka2; // ini sama saja dengan angka1 * angka2 
       System.out.println(angka1);
       angka1 /= angka2; // ini sama saja dengan angka1 / angka2 
       System.out.println(angka1);
    }
    
}
